#ifndef MATROZ
#define MATROZ
//-------------------------------------------------------------------------------------------------
#include <systemc.h>
//-------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------
struct ClockGenerator : public sc_module
{

  sc_out  < bool >  ClkOut;
  SC_CTOR(ClockGenerator) { SC_THREAD(FunctionThread); }
  void FunctionThread() {
    while (1) {
      ClkOut.write(false);
      wait(100,SC_NS);
      ClkOut.write(true);
      wait(100,SC_NS);
    }
  }

};
//-------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------
#endif /* COMPONENTS */
